    <h4>Fungsi Perpustakan Abal-Abal Andy</h4>
    <p>
    <h6>1. Fungsi Edukatif</h6>
    Perpustakaan berfungsi sebagai tempat untuk belajar secara mandiri, di situ
    pengguna dapat mencari bahan-bahan yang dibutuhkan untuk menambah ilmu
    dan wawasan.
    </p>  
    <h6>2. Fungsi Informatif.</h6>
    <p>
    Perpustakaan mempunyai fungsi informatif, artinya informasi yang dibutuhkan
    oleh pengguna dapat dicari di perpustakaan
    </p> 
    <h6>3. Fungsi Penelitian</h6>
    <p>
    Perpustakaan mempunyai fungsi penelitian, artinya, sumber-sumber informasi
    yang ada di dalam perpustakaan tersebut dapat digunakan sebagai bahan rujukan
    untuk melakukan penelitian.

    <p>
        Dengan adanya fungsi perpustakaan yang beragam tersebut, diharapkan para pengguna akan tertarik, sehingga sebanyak dan sesering mungkin datang ke
